#[repr(u8)]
#[derive(Debug, Clone, Copy, PartialEq, Eq)]
pub enum ErrorCode {
    VersionMismatch = 0x01,
    AuthFailed = 0x02,
    SessionExpired = 0x03,
    PermissionDenied = 0x04,
    ChannelNotFound = 0x05,
    RateLimited = 0x06,
    InvalidPacket = 0x07,
    NodeUnavailable = 0x08,
    GuildNotFound = 0x09,
    Blocked = 0x0A,
    Internal = 0xFF,
}

impl ErrorCode {
    pub fn from_u32(v: u32) -> Option<Self> {
        match v {
            0x01 => Some(Self::VersionMismatch),
            0x02 => Some(Self::AuthFailed),
            0x03 => Some(Self::SessionExpired),
            0x04 => Some(Self::PermissionDenied),
            0x05 => Some(Self::ChannelNotFound),
            0x06 => Some(Self::RateLimited),
            0x07 => Some(Self::InvalidPacket),
            0x08 => Some(Self::NodeUnavailable),
            0x09 => Some(Self::GuildNotFound),
            0x0A => Some(Self::Blocked),
            0xFF => Some(Self::Internal),
            _ => None,
        }
    }
}
